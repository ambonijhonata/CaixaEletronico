    public void guardarConta(Conta conta) {
        this.conta.setCodigo(conta.getCodigo());
        this.conta.setDigitoVerificador(conta.getDigitoVerificador());
        this.conta.setTipoConta(conta.getTipoConta());
        this.conta.setAgencia(conta.getAgencia());
        this.conta.setCliente(conta.getCliente());
        this.conta.setSaldo(conta.getSaldo());
    }

