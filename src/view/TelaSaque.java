    Double valorSaque = null;

    private void realizarSaque(Double valorSaque) {
        Conexao conexao = new Conexao();
        ContaDB contaDB = new ContaDB(conexao);
        Double saldoConta = contaDB.getSaldoConta(conta.getCodigo(), conta.getDigitoVerificador());
        if (saldoConta > valorSaque) {
            String senha = mensagemSenha();
            Boolean senhaValida = contaDB.validarSenha(senha, conta.getCodigo());

            if (senhaValida) {
                Boolean sucessoSaque = contaDB.sacar(valorSaque, conta.getCodigo());
                if (sucessoSaque) {
                    JOptionPane.showMessageDialog(null, "Saque realizado com sucesso.", "Saque", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao realizar saque.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Senha inválida.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Saldo insuficiente.", "Atenção", JOptionPane.WARNING_MESSAGE);
            TelaInicial tela = new TelaInicial(conta);
            this.dispose();
            tela.setVisible(true);
        }
        this.dispose();
    }

